<template >
  <form @submit.prevent>
    <h2>Создание поста</h2>
    <input @input="title = $event.target.value" v-bind:value="title" type="text" placeholder="Название">
    <input @input="body = $event.target.value" v-bind:value="body" type="text" placeholder="Описание">
    <button @click="createPost">Добавить</button>
  </form>

  <div class="post" v-for="post in posts">
    <div><strong>Название:</strong> {{ post.title }}</div>
    <div><strong>Описание:</strong> {{ post.body }}</div>
  </div>
</template>

<script>
export default {
  data() {
    return {
      posts: [
        { id: 1, title: 'JavaScript', body: 'Описание поста' },
        { id: 2, title: 'JavaScript 2', body: 'Описание поста 2' },
        { id: 3, title: 'JavaScript 3', body: 'Описание поста 3' },
      ],
      title: '',
      body: '',
    }
  },
  methods: {
    createPost() {
      const newPost = {
        id: Date.now(),
        title: this.title,
        body: this.body,
      }
      this.posts.push(newPost);
      this.title = '';
      this.body = '';

    },
  },
}
</script>

